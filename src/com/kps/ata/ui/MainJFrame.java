    private void btnAdmModVehicleSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdmModVehicleSubmitActionPerformed
        // TODO add your handling code here:
        String vID=txtAdmModVehicleID.getText();
        VehicleBean vBean=null;
        boolean flag=false;
        try{
            vBean=AdminService.getVehicleDetails(vID);
            vBean.getName();
            flag=true;
        }catch(NullPointerException e){
            flag=false;
        }
        if(flag){
            lblAdmVehicleModifyNews.setText("Vehicle Found!");
            
            lblModify1.setVisible(true);
            lblModify2.setVisible(true);
            lblModify3.setVisible(true);
            lblModify4.setVisible(true);
            lblModify5.setVisible(true);
            lblModify6.setVisible(true);
            
            txtAdmVehicleModifyID.setEnabled(false);
            txtAdmVehicleModifyID.setVisible(true);
            txtAdmVehicleModifyRNo.setEnabled(false);
            txtAdmVehicleModifyRNo.setVisible(true);
            radioAdmVehicleModifyAC.setVisible(true);
            radioAdmVehicleModifyNonAC.setVisible(true);
            txtAdmVehicleModifyName.setVisible(true);
            txtAdmVehicleModifyCapacity.setVisible(true);
            txtAdmVehicleModifyFare.setVisible(true);
        
            btnAdmVehicleModifyCancel.setVisible(true);
            btnAdmVehicleModifyUpdate.setVisible(true);
        }
        else{
            lblAdmVehicleModifyNews.setText("No vehicle found!");
            return;
        }
        
        txtAdmVehicleModifyID.setText(vID);
        txtAdmVehicleModifyRNo.setText(vBean.getRegistrationNumber());
        if(vBean.getType().equals("AC")){
            radioAdmVehicleModifyAC.setSelected(true);
            radioAdmVehicleModifyNonAC.setSelected(false);
        }
        else{
            radioAdmVehicleModifyAC.setSelected(false);
            radioAdmVehicleModifyNonAC.setSelected(true);
        }
        
        txtAdmVehicleModifyName.setText(vBean.getName());
        txtAdmVehicleModifyCapacity.setText(Integer.toString(vBean.getSeatingCapacity()));
        txtAdmVehicleModifyFare.setText(Double.toString(vBean.getFarePerKM()));
        
    }//GEN-LAST:event_btnAdmModVehicleSubmitActionPerformed

