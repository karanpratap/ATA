    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        CardLayout card=(CardLayout)mainPanel.getLayout();
        int succ=CustomerService.authenticate(txtUserName.getText(),txtPasswordLogin.getText());
        switch (succ) {
            case 1:
                lblMessage.setText("Welcome Admin!");
                txtUserName.setText(null);
                txtPasswordLogin.setText(null);
                
                card.show(mainPanel, "adminDashboardCard");
                break;
            case 2:
                lblMessage.setText("Welcome customer!");
                txtUserName.setText(null);
                txtPasswordLogin.setText(null);
             
                card.show(mainPanel, "cusDashboardCard");
                btnCusBookConfirm.setVisible(false);
                break;
            default:
                lblMessage.setText("UserID or password Invalid");
                txtPasswordLogin.setText(null);
                break;
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

